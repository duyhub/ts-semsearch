"""Stress evaluation at 1000 POIs -> reports/stress-1000.md (SPEC §11 companion).

Measures the DETERMINISTIC rules-only engine (provider=local, mode=local, LLM off —
the same measurement pinning as every other entry point) on:

  * the official tune split (40 queries) against BOTH corpora — the official 111 POIs
    and the 1000-POI synthetic superset (official 111 verbatim + 889 distractors,
    data/synth/synth_dataset.xlsx) — as-labeled plus two seeded degradations
    (diacritics stripped; diacritics stripped + one adjacent-char typo);
  * the 150 synthetic labeled queries (data/synth/eval_synth.json, ground truth by
    construction) against the 1000-POI corpus;
  * warm ranking latency percentiles at 1000 POIs.

Caveat printed into the report: official labels on the superset are a PESSIMISTIC
lower bound — a synthetic distractor can genuinely answer a labeled query. The
synth150 set has complete labels by construction and is the fairer scale metric.

  uv run python scripts/stress_eval.py
"""
from __future__ import annotations

import argparse
import random
import statistics
import sys
import time
import unicodedata
from dataclasses import replace
from pathlib import Path

sys.path.insert(0, str(Path(__file__).resolve().parents[1] / "src"))

import json  # noqa: E402

from semsearch.data import EvalQuery, load_eval, load_pois  # noqa: E402
from semsearch.eval import evaluate  # noqa: E402
from semsearch.pipeline import FullPipeline  # noqa: E402
from semsearch.rank import load_weights  # noqa: E402
from semsearch.split import load_split, select  # noqa: E402

SYNTH_XLSX = "data/synth/synth_dataset.xlsx"
SYNTH_EVAL = "data/synth/eval_synth.json"
REPORT = Path("reports/stress-1000.md")
METRICS = ("recall@3", "recall@5", "ndcg@5", "mrr")
DEGRADE_SEED = "20260711"  # fixed: the report must be reproducible from a fresh clone


def strip_diacritics(text: str) -> str:
    text = text.replace("đ", "d").replace("Đ", "D")
    return "".join(c for c in unicodedata.normalize("NFD", text)
                   if unicodedata.category(c) != "Mn")


def add_typo(text: str, seed: str) -> str:
    """One deterministic adjacent-char swap in a random word >= 4 chars."""
    rng = random.Random(f"{DEGRADE_SEED}:{seed}")
    words = text.split()
    eligible = [i for i, w in enumerate(words) if len(w) >= 4]
    if not eligible:
        return text
    i = rng.choice(eligible)
    w = words[i]
    p = rng.randrange(1, len(w) - 1)
    words[i] = w[:p] + w[p + 1] + w[p] + w[p + 2:]
    return " ".join(words)


def _row(name: str, res: dict) -> str:
    cells = " | ".join(f"{res['overall'][m]:.3f}" for m in METRICS)
    return f"| {name} | {res['n']} | {cells} |"


def main() -> None:
    ap = argparse.ArgumentParser()
    ap.add_argument("--out", default=str(REPORT))
    args = ap.parse_args()

    # Measurement pinning (NFR-6 discipline): local provider, local mode -> LLM off,
    # rewrite/gate irrelevant. Nothing here can contact the network.
    pois_off = load_pois()
    pois_syn = load_pois(SYNTH_XLSX)
    pipe_off = FullPipeline(pois_off, weights=load_weights(), provider="local", mode="local")
    pipe_syn = FullPipeline(pois_syn, weights=load_weights(), provider="local", mode="local")
    assert pipe_off._llm_parser is None and pipe_syn._llm_parser is None

    tune = select(load_eval(), load_split(), "tune")
    sets = {
        "clean": tune,
        "no-diacritics": [replace(q, input_query=strip_diacritics(q.input_query)) for q in tune],
        "typo": [replace(q, input_query=add_typo(strip_diacritics(q.input_query), q.query_id))
                 for q in tune],
    }
    synth_q = [EvalQuery(**row) for row in
               json.loads(Path(SYNTH_EVAL).read_text(encoding="utf-8"))]

    lines = [
        "# Stress evaluation — 1000-POI corpus (script-generated)",
        "",
        "Generated by `scripts/stress_eval.py` (deterministic rules-only engine; "
        "provider=local, mode=local, LLM off — the standard measurement pinning). "
        "The 1000-POI corpus is the official 111 POIs verbatim + 889 seeded synthetic "
        "distractors (`scripts/gen_synth_pois.py`).",
        "",
        "> Caveat: official-query labels on the 1000-POI superset are a pessimistic "
        "lower bound — a synthetic distractor can genuinely answer a labeled query. "
        "The 150-query synthetic set has complete labels by construction and is the "
        "fairer scale metric.",
        "",
        "| set | n | " + " | ".join(METRICS) + " |",
        "|---|---|" + "---|" * len(METRICS),
    ]

    for corpus_name, pipe in (("official-111", pipe_off), ("synth-1000", pipe_syn)):
        for set_name, qs in sets.items():
            res = evaluate(lambda q: pipe.rank_ids(q.input_query), qs)
            lines.append(_row(f"official tune/{set_name} @ {corpus_name}", res))
            print(lines[-1])
    res = evaluate(lambda q: pipe_syn.rank_ids(q.input_query), synth_q)
    lines.append(_row("synth150 @ synth-1000", res))
    print(lines[-1])

    for q in tune[:10]:
        pipe_syn.rank_ids(q.input_query)  # warm
    times = []
    for q in tune:
        t = time.perf_counter()
        pipe_syn.rank_ids(q.input_query)
        times.append((time.perf_counter() - t) * 1000)
    times.sort()
    p95 = times[int(0.95 * len(times)) - 1]
    lat = (f"Warm ranking latency @1000 POIs: p50 {statistics.median(times):.0f} ms, "
           f"p95 {p95:.0f} ms, max {times[-1]:.0f} ms.")
    lines += ["", f"{lat}", ""]
    print(lat)

    Path(args.out).parent.mkdir(parents=True, exist_ok=True)
    Path(args.out).write_text("\n".join(lines), encoding="utf-8")
    print(f"\nwrote {args.out}")


if __name__ == "__main__":
    main()
